def preprocess_and_save(input_file, output_file):
    with open(input_file, "r", encoding="utf-8") as f_in, \
         open(output_file, "w", encoding="utf-8") as f_out:
        for line in tqdm(f_in, total=total_lines, unit="lines", desc="Preprocessing"):
            line = line.strip()
            if not line:
                continue
            doc = nlp(line)
            tokens = []
            for token in doc:
                if token.is_alpha or token.is_digit or token.text in ["$", "€", "-"]:
                    tokens.append(token.text)
            if tokens:
                f_out.write(" ".join(tokens) + "\n")

