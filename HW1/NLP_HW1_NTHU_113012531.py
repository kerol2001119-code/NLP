# 3.TokenizedIterable（buffer shuffle），此處由於想降低記憶體消耗，chatgpt提供較有效率的架構：使用緩衝區批次讀取可以降低記憶體消耗 #詢問chatgpt shuffle的影響後決定shuffle
class TokenizedIterable:
    def __init__(self, filename, shuffle=False, buffer_size=20000):
        self.filename = filename
        self.shuffle = shuffle
        self.buffer_size = buffer_size

    def __iter__(self):
        buffer = []
        with open(self.filename, "r", encoding="utf-8") as f:
            for line in f:
                buffer.append(line.strip().split())
                if len(buffer) >= self.buffer_size:
                    if self.shuffle:
                        random.shuffle(buffer)
                    for tokens in buffer:
                        yield tokens
                    buffer = []

            if buffer:
                if self.shuffle:
                    random.shuffle(buffer)
                for tokens in buffer:
                    yield tokens



